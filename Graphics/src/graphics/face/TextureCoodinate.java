package graphics.face;

/**
 * A Entity class that represents a Texture Coodinate.
 * 
 * @author Tamaran
 *
 */
public class TextureCoodinate {
	
	private float x,y,w;

	public TextureCoodinate(float x, float y, float w) {
		super();
		this.x = x;
		this.y = y;
		this.w = w;
	}

	public float getX() {
		return x;
	}

	public float getY() {
		return y;
	}

	public float getW() {
		return w;
	}

	
}
